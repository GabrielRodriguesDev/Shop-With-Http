import 'package:flutter/cupertino.dart';

class Product with ChangeNotifier {
  //! Qualquer eventual mudan√ßa nessa classe poderemos notificar quem estiver usando-a
  final String id;
  final String name;
  final String description;
  final double price;
  final String imageUrl;
  bool isFavorite;

  Product(
      {required this.id,
      required this.name,
      required this.description,
      required this.price,
      required this.imageUrl,
      this.isFavorite = false});

  void toggleFavorite() {
    isFavorite = !isFavorite;
    notifyListeners();
  }
}
